package vo;

public class RSV {
	String date;
	double k;
	double d;
	double j;
	public RSV(String date, double k, double d, double j) {
		super();
		this.date = date;
		this.k = k;
		this.d = d;
		this.j = j;
	}
	public String getDate() {
		return date;
	}
	public double getK() {
		return k;
	}
	public double getD() {
		return d;
	}
	public double getJ() {
		return j;
	}
	
	
}
